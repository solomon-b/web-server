--------------------------------------------------------------------------------
-- Scheme 1: role-indexed auth

data UserRole = Viewer | Editor | Admin

$(deriveOrdRole ''UserRole)

newtype RoleAuth (r :: UserRole) = RoleAuth {rName :: String}

type instance AuthServerData (AuthProtect "role-auth") = SomeRole RoleAuth

parseRole :: Request -> Maybe (SomeRole RoleAuth)
parseRole req = case lookup "X-Role" (requestHeaders req) of
  Just "viewer" -> Just (someUserRole Viewer (RoleAuth "Reed"))
  Just "editor" -> Just (someUserRole Editor (RoleAuth "Lyxia"))
  Just "admin" -> Just (someUserRole Admin (RoleAuth "Sandy"))
  _ -> Nothing

roleAuthHandler :: AuthHandler Request (SomeRole RoleAuth)
roleAuthHandler = mkAuthHandler $ maybe (throwError err401) pure . parseRole

-- | Statically admin-gated subroutine. The proof comes from Satisfies.
-- IsAtleastAdmin is generated by deriveOrdRole (one alias per constructor).
banUser :: (IsAtleastAdmin userAuth) => RoleAuth userAuth -> String
banUser auth = "banned by " <> rName auth

--------------------------------------------------------------------------------
-- Test API (separate paths)

type RoleViewerAPI = RequireRole "role-auth" 'Viewer :> "viewer" :> Get '[JSON] String

type RoleEditorAPI = RequireRole "role-auth" 'Editor :> "editor" :> Get '[JSON] String

type RoleAdminAPI = RequireRole "role-auth" 'Admin :> "admin" :> Get '[JSON] String

type RoleAPI = RoleViewerAPI :<|> RoleEditorAPI :<|> RoleAdminAPI

roleServer :: Server RoleAPI
roleServer = viewerH :<|> editorH :<|> adminH
  where
    viewerH :: Satisfies 'Viewer RoleAuth -> Handler String
    viewerH (Satisfies _ auth) = pure ("viewer: " <> rName auth)

    editorH :: Satisfies 'Editor RoleAuth -> Handler String
    editorH (Satisfies _ auth) = pure ("editor: " <> rName auth)

    adminH :: Satisfies 'Admin RoleAuth -> Handler String
    adminH (Satisfies UserRoleProof auth) = pure (banUser auth) -- proof flows into banUser

roleApp :: IO Application
roleApp = pure $ serveWithContext (Proxy @RoleAPI) (roleAuthHandler :. EmptyContext) roleServer

--------------------------------------------------------------------------------
-- Test API (same path, role-based fallthrough)
--
-- Routes ordered most-restrictive-first:
--   Admin  -> "admin panel"
--   Editor -> "editor panel"
--   Viewer -> "viewer panel"

type PanelAdminAPI = RequireRole "role-auth" 'Admin :> "panel" :> Get '[JSON] String

type PanelEditorAPI = RequireRole "role-auth" 'Editor :> "panel" :> Get '[JSON] String

type PanelViewerAPI = RequireRole "role-auth" 'Viewer :> "panel" :> Get '[JSON] String

type FallthroughAPI = PanelAdminAPI :<|> PanelEditorAPI :<|> PanelViewerAPI

fallthroughServer :: Server FallthroughAPI
fallthroughServer = pAdmin :<|> pEditor :<|> pViewer
  where
    pAdmin :: Satisfies 'Admin RoleAuth -> Handler String
    pAdmin _ = pure "admin panel"

    pEditor :: Satisfies 'Editor RoleAuth -> Handler String
    pEditor _ = pure "editor panel"

    pViewer :: Satisfies 'Viewer RoleAuth -> Handler String
    pViewer _ = pure "viewer panel"

fallthroughApp :: IO Application
fallthroughApp = pure $ serveWithContext (Proxy @FallthroughAPI) (roleAuthHandler :. EmptyContext) fallthroughServer

--------------------------------------------------------------------------------
-- Test API (auth invocation counting)

countingRoleHandler :: IORef Int -> AuthHandler Request (SomeRole RoleAuth)
countingRoleHandler counter = mkAuthHandler $ \req -> do
  liftIO $ modifyIORef' counter (+ 1)
  maybe (throwError err401) pure (parseRole req)

countingApp :: IORef Int -> IO Application
countingApp counter =
  pure $ serveWithContext (Proxy @FallthroughAPI) (countingRoleHandler counter :. EmptyContext) fallthroughServer

--------------------------------------------------------------------------------
-- Test API (public route behind role gates)
--
-- Regression guard. A trailing unauthenticated route is unreachable when it
-- sits behind RequireRole alternatives on the same path. Because authentication
-- failure is fatal, an unauthenticated request short-circuits at the first
-- RequireRole (401) and never falls through to the public route. That route is
-- dead code here. Only an authenticated user whose role is below every gate
-- could reach it, and no such role exists in UserRole.

type PanelPublicAPI = "panel" :> Get '[JSON] String

type AnonAPI = FallthroughAPI :<|> PanelPublicAPI

anonServer :: Server AnonAPI
anonServer = fallthroughServer :<|> pure "public panel"

anonApp :: IO Application
anonApp = pure $ serveWithContext (Proxy @AnonAPI) (roleAuthHandler :. EmptyContext) anonServer

--------------------------------------------------------------------------------
-- Non-hierarchical example: permission sets

data Permission = PermRead | PermWrite | PermManage

$(deriveMemberRole ''Permission)

data PermAuth (ps :: [Permission]) = PermAuth

type instance AuthServerData (AuthProtect "perm-auth") = SomeRole PermAuth

permAuthHandler :: AuthHandler Request (SomeRole PermAuth)
permAuthHandler = mkAuthHandler $ \req ->
  case lookup "X-Perms" (requestHeaders req) of
    Just "reader" -> pure (somePermission [PermRead] PermAuth)
    Just "editor" -> pure (somePermission [PermRead, PermWrite] PermAuth)
    Just "manager" -> pure (somePermission [PermManage] PermAuth) -- deliberately not PermRead
    _ -> throwError err401

-- Demands the membership constraint (the generated HasPermWrite alias), not a
-- Proof value. Callable from any handler that can release Member 'PermWrite ps.
writeUser :: (HasPermWrite ps) => PermAuth ps -> IO ()
writeUser PermAuth = pure ()

type PermReadAPI = RequireRole "perm-auth" 'PermRead :> "read" :> Get '[JSON] String

type PermWriteAPI = RequireRole "perm-auth" 'PermWrite :> "write" :> Get '[JSON] String

type PermManageAPI = RequireRole "perm-auth" 'PermManage :> "manage" :> Get '[JSON] String

type PermReadWriteAPI = RequireRole "perm-auth" '[PermRead, PermWrite] :> "readwrite" :> Get '[JSON] String

type PermAPI = PermReadAPI :<|> PermWriteAPI :<|> PermManageAPI :<|> PermReadWriteAPI

permServer :: Server PermAPI
permServer = readH :<|> writeH :<|> manageH :<|> readWriteH
  where
    readH :: Satisfies 'PermRead PermAuth -> Handler String
    readH _ = pure "read ok"

    writeH :: Satisfies 'PermWrite PermAuth -> Handler String
    writeH (Satisfies proof auth) = liftIO (withMember proof (writeUser auth)) >> pure "write ok"

    manageH :: Satisfies 'PermManage PermAuth -> Handler String
    manageH _ = pure "manage ok"

    -- The subset proof releases BOTH memberships, so writeUser (which needs
    -- HasPermWrite) is callable with no re-check.
    readWriteH :: Satisfies '[PermRead, PermWrite] PermAuth -> Handler String
    readWriteH (Satisfies proof auth) =
      liftIO (withAllMembers proof (writeUser auth) >> pure "read+write ok")

permApp :: IO Application
permApp = pure $ serveWithContext (Proxy @PermAPI) (permAuthHandler :. EmptyContext) permServer

--------------------------------------------------------------------------------
-- Scheme 3: flat, non-hierarchical roles (exact match via Eq)

data Region = US | EU | APAC

$(deriveEqRole ''Region)

newtype RegionAuth (r :: Region) = RegionAuth {rTenant :: String}

type instance AuthServerData (AuthProtect "region-auth") = SomeRole RegionAuth

parseRegion :: Request -> Maybe (SomeRole RegionAuth)
parseRegion req = case lookup "X-Region" (requestHeaders req) of
  Just "us" -> Just (someRegion US (RegionAuth "acme-us"))
  Just "eu" -> Just (someRegion EU (RegionAuth "acme-eu"))
  Just "apac" -> Just (someRegion APAC (RegionAuth "acme-apac"))
  _ -> Nothing

regionAuthHandler :: AuthHandler Request (SomeRole RegionAuth)
regionAuthHandler = mkAuthHandler $ maybe (throwError err401) pure . parseRegion

-- | EU-only operation. The proof that r ~ 'EU comes from Satisfies, no re-check.
-- IsEU is generated by deriveEqRole (one alias per constructor).
--
-- NOTE: this could be @RegionAuth 'EU@ but we provide @Is<Role>@ aliases for
-- consistency.
gdprExport :: (IsEU r) => RegionAuth r -> String
gdprExport auth = "GDPR export for " <> rTenant auth

type USAPI = RequireRole "region-auth" 'US :> "us" :> Get '[JSON] String

type EUAPI = RequireRole "region-auth" 'EU :> "eu" :> Get '[JSON] String

type APACAPI = RequireRole "region-auth" 'APAC :> "apac" :> Get '[JSON] String

type RegionAPI = USAPI :<|> EUAPI :<|> APACAPI

regionServer :: Server RegionAPI
regionServer = usH :<|> euH :<|> apacH
  where
    usH :: Satisfies 'US RegionAuth -> Handler String
    usH _ = pure "us ok"

    euH :: Satisfies 'EU RegionAuth -> Handler String
    euH (Satisfies RegionProof auth) = pure (gdprExport auth) -- exact proof flows into gdprExport
    apacH :: Satisfies 'APAC RegionAuth -> Handler String
    apacH _ = pure "apac ok"

regionApp :: IO Application
regionApp = pure $ serveWithContext (Proxy @RegionAPI) (regionAuthHandler :. EmptyContext) regionServer

