-- Spec

hdr :: HeaderName -> ByteString -> [Header]
hdr name val = [(name, val), (hAccept, "application/json")]

role :: ByteString -> ByteString -> WaiSession st SResponse
role r path = request "GET" path (hdr "X-Role" r) ""

perms :: ByteString -> ByteString -> WaiSession st SResponse
perms p path = request "GET" path (hdr "X-Perms" p) ""

region :: ByteString -> ByteString -> WaiSession st SResponse
region r path = request "GET" path (hdr "X-Region" r) ""

spec :: Spec
spec = do
  describe "RequireRole (proof-carrying, decidable relation)" $ do
    -- Hierarchical role x route matrix
    --          | /viewer | /editor | /admin
    -- Viewer   |   200   |   403   |  403
    -- Editor   |   200   |   200   |  403
    -- Admin    |   200   |   200   |  200
    -- No auth  |   401   |   401   |  401
    with roleApp $ do
      describe "hierarchical: /viewer (requires Viewer)" $ do
        it "Viewer -> 200" $ role "viewer" "/viewer" `shouldRespondWith` 200
        it "Editor -> 200" $ role "editor" "/viewer" `shouldRespondWith` 200
        it "Admin  -> 200" $ role "admin" "/viewer" `shouldRespondWith` 200
        it "no auth -> 401" $ get "/viewer" `shouldRespondWith` 401

      describe "hierarchical: /editor (requires Editor)" $ do
        it "Viewer -> 403" $ role "viewer" "/editor" `shouldRespondWith` 403
        it "Editor -> 200" $ role "editor" "/editor" `shouldRespondWith` 200
        it "Admin  -> 200" $ role "admin" "/editor" `shouldRespondWith` 200
        it "no auth -> 401" $ get "/editor" `shouldRespondWith` 401

      describe "hierarchical: /admin (requires Admin)" $ do
        it "Viewer -> 403" $ role "viewer" "/admin" `shouldRespondWith` 403
        it "Editor -> 403" $ role "editor" "/admin" `shouldRespondWith` 403
        it "Admin  -> 200, ban proof flows through" $
          role "admin" "/admin" `shouldRespondWith` "\"banned by Sandy\"" {matchStatus = 200}
        it "no auth -> 401" $ get "/admin" `shouldRespondWith` 401

    with fallthroughApp $ do
      describe "same path, role-based fallthrough" $ do
        it "Admin  -> admin panel" $
          role "admin" "/panel" `shouldRespondWith` "\"admin panel\"" {matchStatus = 200}
        it "Editor -> editor panel (falls through Admin)" $
          role "editor" "/panel" `shouldRespondWith` "\"editor panel\"" {matchStatus = 200}
        it "Viewer -> viewer panel (falls through Admin, Editor)" $
          role "viewer" "/panel" `shouldRespondWith` "\"viewer panel\"" {matchStatus = 200}
        it "no auth -> 401 (auth failure is fatal)" $ get "/panel" `shouldRespondWith` 401

    with anonApp $ do
      describe "public route behind RequireRole is unreachable" $ do
        it "no auth -> 401, not the public route" $ get "/panel" `shouldRespondWith` 401
        it "Viewer -> viewer panel (public route is dead code)" $
          role "viewer" "/panel" `shouldRespondWith` "\"viewer panel\"" {matchStatus = 200}

    describe "auth handler invocation count (re-auth per alternative)" $ do
      it "Admin matches first alternative: auth runs 1x" $ do
        c <- newIORef 0
        app <- countingApp c
        flip runWaiSession app $ role "admin" "/panel" `shouldRespondWith` 200
        readIORef c `shouldReturn` 1
      it "Editor falls through 1 alternative: auth runs 2x" $ do
        c <- newIORef 0
        app <- countingApp c
        flip runWaiSession app $ role "editor" "/panel" `shouldRespondWith` 200
        readIORef c `shouldReturn` 2
      it "Viewer falls through 2 alternatives: auth runs 3x" $ do
        c <- newIORef 0
        app <- countingApp c
        flip runWaiSession app $ role "viewer" "/panel" `shouldRespondWith` 200
        readIORef c `shouldReturn` 3

    -- Non-hierarchical permission-set matrix (membership, not order).
    -- /readwrite requires a list '[PermRead, PermWrite]: needs BOTH (subset).
    --          | /read | /write | /manage | /readwrite
    -- reader   |  200  |  403   |  403    |    403
    -- editor   |  200  |  200   |  403    |    200
    -- manager  |  403  |  403   |  200    |    403
    -- No auth  |  401  |  401   |  401    |    401
    with permApp $ do
      describe "non-hierarchical: reader holds {PermRead}" $ do
        it "/read      -> 200" $ perms "reader" "/read" `shouldRespondWith` 200
        it "/write     -> 403" $ perms "reader" "/write" `shouldRespondWith` 403
        it "/manage    -> 403" $ perms "reader" "/manage" `shouldRespondWith` 403
        it "/readwrite -> 403 (holds PermRead but not PermWrite)" $
          perms "reader" "/readwrite" `shouldRespondWith` 403

      describe "non-hierarchical: editor holds {PermRead, PermWrite}" $ do
        it "/read      -> 200" $ perms "editor" "/read" `shouldRespondWith` 200
        it "/write     -> 200" $ perms "editor" "/write" `shouldRespondWith` 200
        it "/manage    -> 403" $ perms "editor" "/manage" `shouldRespondWith` 403
        it "/readwrite -> 200 (holds both PermRead and PermWrite)" $
          perms "editor" "/readwrite" `shouldRespondWith` "\"read+write ok\"" {matchStatus = 200}

      describe "non-hierarchical: manager holds {PermManage} only" $ do
        it "/manage    -> 200" $ perms "manager" "/manage" `shouldRespondWith` 200
        it "/read      -> 403 (PermManage does NOT imply PermRead)" $
          perms "manager" "/read" `shouldRespondWith` 403
        it "/write     -> 403" $ perms "manager" "/write" `shouldRespondWith` 403
        it "/readwrite -> 403 (holds neither PermRead nor PermWrite)" $
          perms "manager" "/readwrite" `shouldRespondWith` 403

      it "no auth -> 401" $ get "/read" `shouldRespondWith` 401
      it "no auth -> 401 (/readwrite)" $
        get "/readwrite" `shouldRespondWith` 401

    -- Flat exact-match region matrix (equality, not hierarchy or membership)
    --          | /us | /eu | /apac
    -- us       | 200 | 403 | 403
    -- eu       | 403 | 200 | 403
    -- apac     | 403 | 403 | 200
    -- No auth  | 401 | 401 | 401
    with regionApp $ do
      describe "exact-match: US serves only /us" $ do
        it "/us    -> 200" $ region "us" "/us" `shouldRespondWith` 200
        it "/eu    -> 403" $ region "us" "/eu" `shouldRespondWith` 403
        it "/apac  -> 403" $ region "us" "/apac" `shouldRespondWith` 403

      describe "exact-match: EU serves only /eu" $ do
        it "/eu    -> 200, GDPR proof flows through" $
          region "eu" "/eu" `shouldRespondWith` "\"GDPR export for acme-eu\"" {matchStatus = 200}
        it "/us    -> 403" $ region "eu" "/us" `shouldRespondWith` 403
        it "/apac  -> 403" $ region "eu" "/apac" `shouldRespondWith` 403

      describe "exact-match: APAC serves only /apac" $ do
        it "/apac  -> 200" $ region "apac" "/apac" `shouldRespondWith` 200
        it "/us    -> 403" $ region "apac" "/us" `shouldRespondWith` 403

      it "no auth -> 401" $ get "/us" `shouldRespondWith` 401

    describe "developer experience: a role type with no deriver" $
      it "yields a guiding 'No Decidable instance' error" $ do
        result <- try (evaluate underivedDecidable) :: IO (Either SomeException ())
        case result of
          Left e -> show e `shouldContain` "No Decidable instance"
          Right () -> expectationFailure "expected the guiding error, but it type-checked"
